    private void txtPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPassActionPerformed

        try {
            MessageDigest digest = MessageDigest.getInstance("SHA-256");
            String pas = new String(txtPass.getPassword());
            byte[] hash = digest.digest(pas.getBytes(StandardCharsets.UTF_8));
            pass = Base64.getEncoder().encodeToString(hash);

        } catch (NoSuchAlgorithmException ex) {

        }

    }//GEN-LAST:event_txtPassActionPerformed

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        this.txtUsernameActionPerformed(evt);
        this.txtPassActionPerformed(evt);
        txtUsername.setText("");
        txtPass.setText("");
        lblError.setText("");

        try {
            ResultSet rs = Auth.login(username);
            if (!rs.isBeforeFirst()) {
                lblError.setText("Wrong username or password");
            } else {
                while (rs.next()) {
                    if (rs.getString("password").equals(pass)) {
                        System.out.println(rs.getString("username") + "\t" + rs.getString("email") + "\t" + rs.getString("password"));
                    } else {
                        lblError.setText("Wrong username or password");
                    }
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

